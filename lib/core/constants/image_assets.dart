  static String? posterAssetForEventMetadata({
    String? eventId,
    String? title,
    String? promoter,
    DateTime? eventDate,
    String? streamUrl,
    String? ticketUrl,
    bool preferThumb = false,
  }) {
    final exact = _posterAssetForEventId(eventId, preferThumb: preferThumb);
    if (exact != null) {
      return exact;
    }

    final fromUrls = _posterKeyFromUrls(
      eventId: eventId,
      streamUrl: streamUrl,
      ticketUrl: ticketUrl,
    );
    if (fromUrls != null) {
      return _posterAssetForKey(fromUrls, preferThumb: preferThumb);
    }

    final mappedKey = _mappedEventPosterKey(title, promoter);
    if (mappedKey != null &&
        (_matchesExpectedEventDate(mappedKey, eventDate) ||
            _matchesNearbyEventDate(mappedKey, eventDate) ||
            _hasStrongPosterSignal(mappedKey, title, promoter))) {
      return _posterAssetForKey(mappedKey, preferThumb: preferThumb);
    }

    return null;
  }

  static String? posterAssetForEventMetadataVariant({
    String? eventId,
    String? title,
