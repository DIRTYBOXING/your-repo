  static String? _posterKeyFromUrls({
    String? eventId,
    String? streamUrl,
    String? ticketUrl,
  }) {
    final haystack = [eventId, streamUrl, ticketUrl]
        .whereType<String>()
        .map((value) => Uri.decodeFull(value).toLowerCase())
        .join(' ');
    if (haystack.isEmpty) {
      return null;
    }

    for (final entry in _ppvUrlHintMap.entries) {
      if (entry.value.any(haystack.contains)) {
        return entry.key;
      }
    }
    return null;
  }

  static bool _matchesExpectedEventDate(String key, DateTime? eventDate) {
    final expected = _ppvDateMap[key];
    if (expected == null || eventDate == null) {
      return false;
    }

    final parsed = DateTime.tryParse(expected);
    if (parsed == null) {
      return false;
    }

    return eventDate.year == parsed.year &&
        eventDate.month == parsed.month &&
        eventDate.day == parsed.day;
  }

  static bool _matchesNearbyEventDate(
    String key,
    DateTime? eventDate, {
    int toleranceDays = 45,
  }) {
    final expected = _ppvDateMap[key];
    if (expected == null || eventDate == null) {
      return false;
    }

    final parsed = DateTime.tryParse(expected);
    if (parsed == null) {
      return false;
    }

    return eventDate.difference(parsed).inDays.abs() <= toleranceDays;
  }

