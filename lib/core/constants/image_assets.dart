  static String _normalizePosterLookupKey(String value) {
    final normalized = value
        .trim()
        .toLowerCase()
        .replaceAll(RegExp(r'[^a-z0-9]+'), '-')
        .replaceAll(RegExp(r'-+'), '-');
    return normalized.replaceAll(RegExp(r'^-|-$'), '');
  }

  static bool _isKnownPpvKey(String key) {
    return _ppvHeroMap.containsKey(key) ||
        _ppvThumbMap.containsKey(key) ||
        _ppvBannerMap.containsKey(key);
  }

  static String _posterVariantPath(String key, String variant) {
    return '$_ppvDir/${key}_${variant.toLowerCase()}.jpg';
  }

  static String? _posterAssetForVariant(String key, String variant) {
    if (!_isKnownPpvKey(key)) {
      return null;
    }

    switch (variant.toLowerCase()) {
      case 'thumb':
        return _ppvThumbMap[key] ?? _posterVariantPath(key, 'thumb');
      case 'banner':
        return _ppvBannerMap[key] ?? _posterVariantPath(key, 'banner');
      case 'preview':
        return _posterVariantPath(key, 'preview');
      case 'portrait':
        return _posterVariantPath(key, 'portrait');
      case 'hero':
      default:
        return _ppvHeroMap[key] ?? _posterVariantPath(key, 'hero');
    }
  }

  static String? _posterKeyFromAssetUrl(String? url) {
    if (url == null || url.trim().isEmpty) {
      return null;
    }

    final normalized = url.trim().toLowerCase();
    final match = RegExp(
      r'assets/ppv/(ppv-[a-z0-9-]+)_(hero|thumb|banner|preview|portrait)\.(jpg|webp)$',
    ).firstMatch(normalized);
    return match?.group(1);
  }

  static String? _posterAssetForKey(String key, {bool preferThumb = false}) {
    return _posterAssetForVariant(key, preferThumb ? 'thumb' : 'hero');
  }

  static String? _posterAssetForEventId(
    String? eventId, {
    bool preferThumb = false,
  }) {
    if (eventId == null || eventId.trim().isEmpty) {
      return null;
    }

    final normalized = _normalizePosterLookupKey(eventId);
    final candidates = <String>{eventId.trim(), normalized};
    final aliased = _ppvEventAliasMap[normalized];
    if (aliased != null) {
      candidates.add(aliased);
    }
    for (final value in List<String>.from(candidates)) {
      if (value.startsWith('demo-')) {
        candidates.add(value.substring(5));
      }
      if (value.startsWith('ppv-')) {
        candidates.add(value.substring(4));
      } else {
        candidates.add('ppv-$value');
      }
    }

    for (final candidate in candidates) {
      final resolved = _posterAssetForKey(candidate, preferThumb: preferThumb);
      if (resolved != null) {
        return resolved;
      }
    }
    return null;
  }

