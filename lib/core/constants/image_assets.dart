  static String posterVariantFromUrl(String? url, {required String variant}) {
    final normalized = url?.trim();
    if (normalized == null || normalized.isEmpty) {
      return '';
    }

    final key = _posterKeyFromAssetUrl(normalized);
    if (key == null) {
      return normalized;
    }

    return _posterAssetForVariant(key, variant) ?? normalized;
  }

  static bool isSpecificEventPosterAsset(String? url) {
    if (url == null || url.trim().isEmpty) {
      return false;
    }

    final normalized = url.trim();
    return RegExp(
      r'^assets/ppv/ppv-[a-z0-9-]+_(hero|thumb|banner|preview|portrait)\.(jpg|webp)$',
    ).hasMatch(normalized);
  }

  static bool isGenericPosterAsset(String? url) {
    if (url == null) return true;
    final normalized = url.trim();
    if (normalized.isEmpty) return true;
    return normalized == bgHero ||
        normalized == bgAction ||
        normalized == bgEvent ||
        normalized == bgPromo ||
        normalized == bgLogo1024 ||
        normalized == bgLogoSmall ||
        normalized == bgCentral ||
        normalized == bgResized ||
        normalized == bgSquare;
  }

  /// Returns a branded poster image for the given sport type.
  static String posterForSport(String? sportType) {
    switch (sportType?.toLowerCase()) {
      case 'mma':
        return bgAction;
      case 'boxing':
        return bgEvent;
      case 'kickboxing':
        return bgPromo;
      case 'muay thai':
        return bgCentral;
      case 'bkfc':
      case 'bare knuckle':
        return bgHero;
      case 'brawling':
        return bgSquare;
      case 'pro wrestling':
        return bgCentral;
      case 'drone racing':
        return bgResized;
      default:
        return bgLogoSmall;
    }
  }

  /// Returns `true` when the URL is a local asset path (not http).
  static bool isLocalAsset(String url) =>
      url.startsWith('assets/') || url.startsWith('asset:');

  /// Smart image widget — handles both local assets and network URLs safely.
  /// Sanitizes URLs through ImageUrlSanitizer before resolving.
  static ImageProvider resolveImage(String url) {
    final safe = ImageUrlSanitizer.sanitize(
      url,
    );
    if (isLocalAsset(safe)) {
      return AssetImage(safe);
    }
    return NetworkImage(safe);
  }

  /// Sanitize + resolve: the ONE call every screen should use for ImageProvider.
  /// Handles null, empty, blocked, asset paths, and network URLs.
  static ImageProvider safeProvider(String? url, {String? fallback}) {
    final safe = ImageUrlSanitizer.sanitize(
      url,
      fallback: fallback ?? dfcBrandedPlaceholder,
    );
    return resolveImage(safe);
  }

