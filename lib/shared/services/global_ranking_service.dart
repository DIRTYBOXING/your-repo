/// A single entry in the global ranking leaderboard.
class GlobalRankEntry {
  final String id;
  final String name;
  final String weightClass;
  final String sport;
  final double hypeScore;
  final double momentumScore;
  final double globalScore;
  final int wins;
  final int losses;
  final bool isChampion;
  final String region;
  final String gender; // 'M' or 'F'

  const GlobalRankEntry({
    required this.id,
    required this.name,
    required this.weightClass,
    required this.sport,
    required this.hypeScore,
    required this.momentumScore,
    required this.globalScore,
    required this.wins,
    required this.losses,
    required this.isChampion,
    required this.region,
    required this.gender,
  });

  String get record => '$wins-$losses';
}
