/// GlobalRankingService — hype score + momentum score engine for fighter/gym/event global ranking.
class GlobalRankingService {
  static final GlobalRankingService _instance =
      GlobalRankingService._internal();
  factory GlobalRankingService() => _instance;
  GlobalRankingService._internal();

  /// Compute a hype score (0–100) from engagement + reach + recency.
  /// [engagement]  total likes + comments + shares
  /// [reach]       estimated audience size
  /// [daysSinceActive] days since last active content
  double computeHypeScore({
    required int engagement,
    required int reach,
    required int daysSinceActive,
  }) {
    if (reach <= 0) return 0;
    final engagementRate = (engagement / reach).clamp(0.0, 1.0);
    final recencyBoost = (1 / (1 + daysSinceActive * 0.1)).clamp(0.0, 1.0);
    final raw = (engagementRate * 60) + (recencyBoost * 40);
    return raw.clamp(0.0, 100.0);
  }

  /// Compute a momentum score (0–100) from trajectory over recent periods.
  /// [previousEngagement] engagement in the prior period
  /// [currentEngagement]  engagement in the current period
  double computeMomentumScore({
    required int previousEngagement,
    required int currentEngagement,
  }) {
    if (previousEngagement <= 0) return 50.0;
    final delta = currentEngagement - previousEngagement;
    final ratio = delta / previousEngagement;
    // Map ratio from [-1, +1] → [0, 100]
    return ((ratio + 1) * 50).clamp(0.0, 100.0);
  }

  /// Compute a composite global rank score.
  double globalScore({
    required double hypeScore,
    required double momentumScore,
    required int wins,
    required int losses,
    bool isChampion = false,
  }) {
    final record = wins + losses > 0 ? wins / (wins + losses) : 0.5;
    final championBonus = isChampion ? 15.0 : 0.0;
    final raw =
        (hypeScore * 0.4) +
        (momentumScore * 0.3) +
        (record * 100 * 0.3) +
        championBonus;
    return raw.clamp(0.0, 100.0);
  }

  /// Returns a rank label from a global score.
  String rankLabel(double score) {
    if (score >= 90) return 'ELITE';
    if (score >= 75) return 'TOP 10';
    if (score >= 60) return 'RANKED';
    if (score >= 40) return 'RISING';
    return 'PROSPECT';
  }

